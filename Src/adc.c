uint16_t get_temperature(){
  int16_t temperature;
  uint16_t value;

  HAL_ADC_Start(&hadc1);
  HAL_ADC_PollForConversion(&hadc1, 1);
  value = (uint16_t) HAL_ADC_GetValue(&hadc1);
  HAL_ADC_Stop(&hadc1);

  temperature = value * VREF_THEORETICAL / VREF_CALIBRATION;
  temperature = temperature - *TS_CAL_1;
  temperature = temperature * (TS_TEMP_2 - TS_TEMP_1) / (*TS_CAL_2 - *TS_CAL_1);
  temperature = temperature + TS_TEMP_1;

  return temperature;
}

